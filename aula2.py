# sep= funciona como um separador.
# Quebra de linha - end="\r\n" - sistema unix (windows)
# Quebra de linha - end="\n" - linux
print(12, 34, sep="-", end="##\n") 
print(56, 78, sep=" ", end="\n")
# \r\n - quebra de linha - CRLF
# não usar Letras maiúsculas, pois pode ser outra função ou inexistente
